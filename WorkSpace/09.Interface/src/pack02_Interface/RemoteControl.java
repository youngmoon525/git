package pack02_Interface;

public interface RemoteControl {
	//인터페이스에서의 변수를 만든다( 상 수 )
	//인터페이스에서 변수를 만들면 자동으로 앞에 final이 붙었다고 생각하면됨 
	
	int MAX_VOLUME = 10;
	int MIN_VOLUME = 0;
	
	
	
	//추상 메소드
	//Interface에서 메소드를 만들게 되면 기본적으로 추상메소드
	//abstract가 앞에 붙어있지만 전부 추상메소드 이기 떄문에 생략해서 보여줌
	
	void turnOn();
	//두메소드는 달리 보이지만 똑같다.
	abstract void turnOff();
	
	void setVolume(int volume);
}
