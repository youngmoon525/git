package pack03_VariableType;

public class Ex01_Number {
	public static void main(String[] args) {
		short s = 10000;
		System.out.println("short s의 값 : " + s);
		int i = 1234567890;
		System.out.println("int i의 값 : " + i);
		long l = 12345678900L;//long타입의 변수를 사용하려면 값의 뒤에 L
		//이 반드시 들어가야함
		System.out.println("long l의 값 : " + l);
		
	}//main
}//class
