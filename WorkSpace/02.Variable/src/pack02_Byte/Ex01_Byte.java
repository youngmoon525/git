package pack02_Byte;

public class Ex01_Byte {
	public static void main(String[] args) {
		//2승수로 용량이 정해짐
		//8bit = 1byte
		//bit는 0이나 1의 두가지 정보를 표현할 수 있는 최소의 정보 표현 단위
		//최대값은 127 , 최소값은 -128
		//변수타입 변수명  = 값; 
		byte var1 = -128;
		byte var2 = 127;
		System.out.println(var1);
		System.out.println(var2);
		
	}
}
